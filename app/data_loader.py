HEADERS = {"User-Agent": "AITravelGuide/1.0 (travel-guide-project; jhwwon32@gmail.com)"}

def fetch_wikipedia_data(query: str) -> str:
    try:
        encoded_query = quote(query)
        url = f"https://ko.wikipedia.org/api/rest_v1/page/summary/{encoded_query}"
        response = requests.get(url, headers=HEADERS, timeout=10)
        if response.status_code == 200:
            data = response.json()
            return data.get("extract", "")
    except Exception:
        pass
    return ""

def load_all_travel_data() -> list[Document]:
    print("=== 여행 데이터 수집 시작 ===")
    docs = []

    print("--- 국내 여행지 수집 중 ---")
    for dest in DOMESTIC_DESTINATIONS:
        content = fetch_wikipedia_data(dest)
        if content:
            docs.append(Document(
                page_content=f"{dest} 여행 정보:\n{content}",
                metadata={"region": dest, "type": "domestic", "source": "위키피디아"}
            ))
            print(f"[OK] {dest} 완료")
        else:
            print(f"[FAIL] {dest} 실패")

    print("--- 해외 여행지 수집 중 ---")
    for dest in INTERNATIONAL_DESTINATIONS:
        content = fetch_wikipedia_data(dest)
        if content:
            docs.append(Document(
                page_content=f"{dest} 여행 정보:\n{content}",
                metadata={"region": dest, "type": "international", "source": "위키피디아"}
            ))
            print(f"[OK] {dest} 완료")
        else:
            print(f"[FAIL] {dest} 실패")

    print(f"=== 총 {len(docs)}개 문서 수집 완료 ===")
    return docs
